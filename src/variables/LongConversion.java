package variables;

public class LongConversion {
	public static void main(String[]args) {

		
		long b = 43l;
		
        int a = (int)b;
		byte by = (byte)b;
		short s = (byte)b;
		float f = (byte)b;
		double d = (byte)b;
		char c = (char)b;
		 
		System.out.println(by);
		System.out.println(b);
		System.out.println(s);
		System.out.println(f);
		System.out.println(d);
		System.out.println(c);
	}
}
