package variables;

public class CharConversion {
	public static void main(String[]args) {
		
		char c = 'A';
		
	    double d = c;
		
		float f = (float)c;
		short s = (short)c;
        long b = (long)c;
        int a = (int)c;
		byte by = (byte)c;
		
		 
		System.out.println(by);
		System.out.println(b);
		System.out.println(s);
		System.out.println(f);
		System.out.println(d);
		System.out.println(c);
	}

}
