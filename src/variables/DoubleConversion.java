package variables;

public class DoubleConversion {
public static void main(String[]args) {
	
	    double d = 30.95;
		
		float f = (float)d;
		short s = (short)d;
        long b = (long)d;
        int a = (int)d;
		byte by = (byte)d;
		char c = (char)d;
		 
		System.out.println(by);
		System.out.println(b);
		System.out.println(s);
		System.out.println(f);
		System.out.println(d);
		System.out.println(c);
	}
}
