package variables;

public class ShortConversion {
  public static void main(String[]args) {

		
		
		short s = 4;
	
        long b = s;
        int a = s;
		byte by = (byte)s;
		float f = s;
		double d = s;
		char c = (char)s;
		 
		System.out.println(by);
		System.out.println(b);
		System.out.println(s);
		System.out.println(f);
		System.out.println(d);
		System.out.println(c);
	}

}
