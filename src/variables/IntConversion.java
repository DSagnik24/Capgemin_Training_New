package variables;

public class IntConversion {
	public static void main(String[]args) {

		int a = 40;
		
		byte by = (byte)a;
		long b = (byte)a;
		short s = (byte)a;
		float f = (byte)a;
		double d = (byte)a;
		char c = (char)a;
		 
		System.out.println(by);
		System.out.println(b);
		System.out.println(s);
		System.out.println(f);
		System.out.println(d);
		System.out.println(c);
	}
}
